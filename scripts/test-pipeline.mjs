/**
 * test-pipeline.mjs
 *
 * End-to-end pipeline validation for new card uploads.
 *
 * Steps:
 *   1. Mint a valid session JWT for the owner user
 *   2. Upload a test image via POST /api/upload
 *   3. Verify the returned URL is a correct /manus-storage/... path
 *   4. Save a graded card via tRPC vault.saveCard with that URL
 *   5. Query the DB to confirm frontImageKey is stored correctly
 *   6. Confirm verify.getCard returns frontImageUrl = frontImageKey (no double-prefix)
 *   7. Confirm the URL is suitable for CardSlab3D (same-origin, no external host)
 *   8. Clean up the test card from the DB
 *
 * Run from the project root:
 *   node scripts/test-pipeline.mjs
 *
 * Requires: /tmp/test-card.png (generated by: ffmpeg -f lavfi -i color=c=blue:size=100x140:rate=1 -frames:v 1 /tmp/test-card.png -y)
 */

import "dotenv/config";
import { readFileSync } from "fs";
import mysql from "mysql2/promise";
import { SignJWT } from "jose";
import FormData from "form-data";
import fetch from "node-fetch";

const BASE_URL     = "http://localhost:3000";
const COOKIE_NAME  = "app_session_id";
const JWT_SECRET   = process.env.JWT_SECRET;
const DATABASE_URL = process.env.DATABASE_URL;
const OWNER_OPEN_ID = process.env.OWNER_OPEN_ID;
const VITE_APP_ID   = process.env.VITE_APP_ID;

if (!JWT_SECRET || !DATABASE_URL || !OWNER_OPEN_ID) {
  console.error("❌  Missing required env vars: JWT_SECRET, DATABASE_URL, OWNER_OPEN_ID");
  process.exit(1);
}

// ── Helpers ───────────────────────────────────────────────────────────────────

async function mintSession() {
  const secretKey = new TextEncoder().encode(JWT_SECRET);
  const exp = Math.floor((Date.now() + 365 * 24 * 60 * 60 * 1000) / 1000);
  return new SignJWT({ openId: OWNER_OPEN_ID, appId: VITE_APP_ID ?? "", name: "Test" })
    .setProtectedHeader({ alg: "HS256", typ: "JWT" })
    .setExpirationTime(exp)
    .sign(secretKey);
}

// ── Main ──────────────────────────────────────────────────────────────────────

async function run() {
  const conn = await mysql.createConnection(DATABASE_URL);
  let testCardId = null;

  const DOUBLE_PREFIX = "/manus-storage/manus-storage/";
  const SINGLE_PREFIX = "/manus-storage/";

  try {
    // ── Step 1: Mint session ──────────────────────────────────────────────────
    console.log("\n🔑  Step 1: Minting session token...");
    const token = await mintSession();
    const cookieHeader = `${COOKIE_NAME}=${token}`;
    console.log("    ✅  Session minted");

    // ── Step 2: Upload image ──────────────────────────────────────────────────
    console.log("\n📤  Step 2: Uploading test image to /api/upload...");
    const imgBuffer = readFileSync("/tmp/test-card.png");
    const form = new FormData();
    form.append("image", imgBuffer, { filename: "test-card.png", contentType: "image/png" });

    const uploadResp = await fetch(`${BASE_URL}/api/upload`, {
      method: "POST",
      headers: { ...form.getHeaders(), cookie: cookieHeader },
      body: form,
    });

    if (!uploadResp.ok) {
      const text = await uploadResp.text();
      throw new Error(`Upload failed (${uploadResp.status}): ${text}`);
    }

    const { url: uploadedUrl, key: uploadedKey } = await uploadResp.json();
    console.log("    Returned URL:", uploadedUrl);
    console.log("    Returned key:", uploadedKey);

    // ── Step 3: Validate returned URL ────────────────────────────────────────
    console.log("\n🔍  Step 3: Validating returned URL...");

    if (!uploadedUrl.startsWith(SINGLE_PREFIX)) {
      throw new Error(`URL does not start with /manus-storage/: "${uploadedUrl}"`);
    }
    if (uploadedUrl.startsWith(DOUBLE_PREFIX)) {
      throw new Error(`URL has double-prefix: "${uploadedUrl}"`);
    }
    if (uploadedUrl.startsWith("http://") || uploadedUrl.startsWith("https://")) {
      throw new Error(`URL is external (not same-origin): "${uploadedUrl}"`);
    }
    console.log("    ✅  URL is a correct /manus-storage/... path — no double-prefix, no external host");

    // ── Step 4: Save graded card via tRPC ────────────────────────────────────
    console.log("\n💾  Step 4: Saving graded card via tRPC vault.saveCard...");
    // tRPC v11 batch mutation: POST /api/trpc/vault.saveCard?batch=1
    // Body must be { "0": { json: <input> } } (object keyed by index, not array)
    const trpcBody_save = {
      "0": {
        json: {
          cardName:       "Pipeline Test Card",
          cardSet:        "Test Set",
          cardYear:       "2024",
          cardNumber:     "TEST-001",
          manufacturer:   "Test Manufacturer",
          overallScore:   92.5,
          gradeTier:      "ELITE",
          centeringScore: 90,
          edgesScore:     93,
          cornersScore:   94,
          surfaceScore:   92,
          eyeAppealScore: 95,
          diamondRating:  4,
          frontImageUrl:  uploadedUrl,
          backImageUrl:   uploadedUrl,
        }
      }
    };

    const trpcResp = await fetch(`${BASE_URL}/api/trpc/vault.saveCard?batch=1`, {
      method: "POST",
      headers: { "content-type": "application/json", cookie: cookieHeader },
      body: JSON.stringify(trpcBody_save),
    });

    const trpcBody = await trpcResp.json();
    const result = trpcBody[0];

    if (result?.error) {
      throw new Error(`tRPC saveCard error: ${JSON.stringify(result.error)}`);
    }

    const { cardId, certId } = result?.result?.data?.json ?? {};
    if (!cardId || !certId) {
      throw new Error(`saveCard returned unexpected response: ${JSON.stringify(result)}`);
    }
    testCardId = cardId;
    console.log(`    ✅  Card saved — ID: ${cardId} | certId: ${certId}`);

    // ── Step 5: Verify DB storage ─────────────────────────────────────────────
    console.log("\n🗄️   Step 5: Checking DB for stored frontImageKey...");
    const [rows] = await conn.execute(
      "SELECT id, certId, frontImageKey, backImageKey FROM gradedCards WHERE id = ?",
      [cardId]
    );
    const card = rows[0];
    if (!card) throw new Error(`Card ID ${cardId} not found in DB`);

    console.log("    frontImageKey:", card.frontImageKey);
    console.log("    backImageKey: ", card.backImageKey);

    if (!card.frontImageKey) {
      throw new Error("frontImageKey is NULL in DB — URL was not saved");
    }
    if (card.frontImageKey.startsWith(DOUBLE_PREFIX)) {
      throw new Error(`frontImageKey has double-prefix in DB: "${card.frontImageKey}"`);
    }
    if (!card.frontImageKey.startsWith(SINGLE_PREFIX)) {
      throw new Error(`frontImageKey is not a /manus-storage/ path: "${card.frontImageKey}"`);
    }
    if (card.frontImageKey !== uploadedUrl) {
      throw new Error(`frontImageKey mismatch — stored: "${card.frontImageKey}" vs uploaded: "${uploadedUrl}"`);
    }
    console.log("    ✅  frontImageKey matches uploaded URL exactly — stored correctly in DB");

    // ── Step 6: Verify verify.getCard returns correct frontImageUrl ──────────
    console.log("\n🔗  Step 6: Checking verify.getCard returns correct frontImageUrl...");
    // tRPC v11 batch query: GET /api/trpc/verify.getCard?batch=1&input={"0":{"json":{"certId":"..."}}}  
    const verifyInput = encodeURIComponent(JSON.stringify({ "0": { json: { certId } } }));
    const verifyResp = await fetch(`${BASE_URL}/api/trpc/verify.getCard?batch=1&input=${verifyInput}`, {
      method: "GET",
      headers: { cookie: cookieHeader },
    });
    const verifyBody = await verifyResp.json();
    const verifyResult = verifyBody[0]?.result?.data?.json;

    if (!verifyResult) throw new Error(`verify.getCard returned no data: ${JSON.stringify(verifyBody)}`);

    const returnedFrontImageUrl = verifyResult.frontImageUrl;
    console.log("    verify.getCard frontImageUrl:", returnedFrontImageUrl);

    if (!returnedFrontImageUrl) {
      throw new Error("verify.getCard returned null frontImageUrl");
    }
    if (returnedFrontImageUrl.startsWith(DOUBLE_PREFIX)) {
      throw new Error(`verify.getCard returned double-prefix URL: "${returnedFrontImageUrl}"`);
    }
    if (returnedFrontImageUrl !== uploadedUrl) {
      throw new Error(`verify.getCard URL mismatch — returned: "${returnedFrontImageUrl}" vs uploaded: "${uploadedUrl}"`);
    }
    console.log("    ✅  verify.getCard returns the exact uploaded URL — no double-prefix");

    // ── Step 7: Confirm slab readiness ───────────────────────────────────────
    console.log("\n🎨  Step 7: Confirming URL is suitable for CardSlab3D...");
    const isRelative   = returnedFrontImageUrl.startsWith("/");
    const isSameOrigin = !returnedFrontImageUrl.startsWith("http");
    const isStorage    = returnedFrontImageUrl.startsWith(SINGLE_PREFIX);
    const noDouble     = !returnedFrontImageUrl.startsWith(DOUBLE_PREFIX);

    console.log(`    Relative path:       ${isRelative ? "✅" : "❌"}`);
    console.log(`    Same-origin:         ${isSameOrigin ? "✅" : "❌"}`);
    console.log(`    /manus-storage/ path:${isStorage ? "✅" : "❌"}`);
    console.log(`    No double-prefix:    ${noDouble ? "✅" : "❌"}`);

    if (!isRelative || !isSameOrigin || !isStorage || !noDouble) {
      throw new Error("URL is not suitable for CardSlab3D TextureLoader");
    }
    console.log("    ✅  URL is same-origin /manus-storage/ path — CardSlab3D TextureLoader will load it correctly");

    // ── Summary ───────────────────────────────────────────────────────────────
    console.log("\n" + "═".repeat(60));
    console.log("✅  ALL 7 STEPS PASSED — pipeline is working end-to-end");
    console.log("═".repeat(60));
    console.log("\nPipeline summary:");
    console.log("  1. Upload  →", uploadedUrl);
    console.log("  2. DB key  →", card.frontImageKey, "✅ matches");
    console.log("  3. Verify  →", returnedFrontImageUrl, "✅ no double-prefix");
    console.log("  4. Slab    → will render real card image ✅");

  } finally {
    if (testCardId) {
      await conn.execute("DELETE FROM gradedCards WHERE id = ?", [testCardId]);
      console.log(`\n🧹  Cleaned up test card ID ${testCardId}`);
    }
    await conn.end();
  }
}

run().catch(err => {
  console.error("\n❌  Pipeline test FAILED:", err.message);
  process.exit(1);
});
